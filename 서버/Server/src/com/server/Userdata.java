package com.server;

public class Userdata {
	private String id = "";
	private String passwd = "";
	private String myphon = "";
	private String link = "";
	private String flag = "";
	
	public void setId(String id)
	{
		this.id = id;
		
	}
	public String getId()
	{
		return this.id;	
	}
	
	public void setpasswd(String passwd)
	{
		this.passwd = passwd;
		
	}
	public String getpasswd()
	{
		return this.passwd;	
	}
	
	public void setmyphon(String myphon)
	{
		this.myphon = myphon;
		
	}
	public String getmyphon()
	{
		return this.myphon;	
	}
	
	public void setlink(String link)
	{
		this.link = link;
		
	}
	public String getlink()
	{
		return this.link;	
	}
	
	public void setflag(String flag)
	{
		this.flag = flag;
		
	}
	public String getflag()
	{
		return this.flag;	
	}
	

}
